from enum import IntEnum

class Command(IntEnum):
    HELLO = 0x01
    HELLO_RESPONSE = 0x81
    START_SESSION = 0x02
    START_SESSION_RESPONSE = 0x82
    ERASE = 0x03
    ERASE_RESPONSE = 0x83
    DOWNLOAD_START = 0x04
    DOWNLOAD_START_RESPONSE = 0x84
    DATA = 0x05
    DATA_RESPONSE = 0x85
    DOWNLOAD_END = 0x06
    DOWNLOAD_END_RESPONSE = 0x86
    RESET = 0x07
    RESET_RESPONSE = 0x87
    ABORT = 0x08
    ABORT_RESPONSE = 0x88
    ERROR_RESPONSE = 0x7F
